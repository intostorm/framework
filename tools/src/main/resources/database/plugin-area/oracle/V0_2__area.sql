CREATE TABLE SYS_AREA (
	RECORD_ID NUMBER(6) NOT NULL ,
	AREACODE VARCHAR(255) NOT NULL,
	AREANAME VARCHAR(255) NOT NULL,
	FULL_AREANAME VARCHAR(255) NULL,
	IS_AVAILABLE DECIMAL(1) DEFAULT 1
);

ALTER TABLE SYS_AREA ADD CONSTRAINT PK_AREA_RECORD_ID PRIMARY KEY (RECORD_ID);

CREATE SEQUENCE S_SYS_AREA
	START WITH 1
	MAXVALUE 9999999999999999999999999999
	MINVALUE 1
	NOCYCLE
	CACHE 20
	NOORDER;
